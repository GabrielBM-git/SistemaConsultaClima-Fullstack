//-------------------------------------------------------------------------------------------------

using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;

//-------------------------------------------------------------------------------------------------

namespace Back_End.Models
{
    //---------------------------------------------------------------------------------------------

    public class Estado
    {
        //-----------------------------------------------------------------------------------------

        [Key]
        public int Id { get; set; }

        //-----------------------------------------------------------------------------------------
        
        [Required]
        [StringLength(200)]
	    public string Nome { get; set; }
	    
        [Required]
        [StringLength(2)]
        public string UF { get; set; }
    
        //-----------------------------------------------------------------------------------------
    }

    //---------------------------------------------------------------------------------------------
}

